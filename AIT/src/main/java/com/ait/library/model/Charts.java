package com.ait.library.model;

public class Charts {
    public int month;
    public int count;

    public Charts(){}

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
